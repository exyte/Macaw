import Foundation

class Insets {

	var top: NSNumber = 0
	var right: NSNumber = 0
	var bottom: NSNumber = 0
	var left: NSNumber = 0

	init(top: NSNumber = 0, right: NSNumber = 0, bottom: NSNumber = 0, left: NSNumber = 0) {
		self.top = top	
		self.right = right	
		self.bottom = bottom	
		self.left = left	
	}

}
