open class Insets {

    public let top: Double
    public let right: Double
    public let bottom: Double
    public let left: Double

    public init(top: Double = 0, right: Double = 0, bottom: Double = 0, left: Double = 0) {
        self.top = top
        self.right = right
        self.bottom = bottom
        self.left = left
    }
}
